use stars::stars;

fn main() {
    println!("{}", stars(1));
    println!("{}", stars(4));
    println!("{}", stars(5));
}

#[cfg(test)]
mod test {
    use super::*;
    #[test]
    fn test_stars() {
        assert_eq!(stars(0), "*");
        assert_eq!(stars(1), "**");
        assert_eq!(stars(2), "****");
        assert_eq!(stars(3), "********");
        assert_eq!(stars(4), "****************");
        assert_eq!(stars(5), "********************************");
        assert_eq!(
            stars(10),
            "****************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************"
        );
    }
}